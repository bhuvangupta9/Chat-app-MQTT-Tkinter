void solve(){
	int n,m;
	cin>>n>>m;
	vector<int> vp(n);
	for(int &i:vp)cin>>i;
	multiset<int> s1 , s2 ;
	int sum1 = 0  , sum2 = 0;
	for(int i:vp){
		while(!s1.empty() && !s2.empty() && *--s1.end() > *s2.begin()){
			int froms1 = *--s1.end() ,  froms2 = *s2.begin();
			s1.erase(--s1.end());
			s2.erase(s2.begin());
			sum1 = sum1 - froms1 + froms2;
			sum2 = sum2 + froms1 - froms2;
			s1.insert(froms2);
			s2.insert(froms1);
		}
		while(sum1+i > m){
			int temp = *--s1.end();
			s1.erase(--s1.end());
			s2.insert(temp);
			sum1 -= temp;
			sum2 += temp;
		}
		while(!s2.empty() && (sum1+i+*s2.begin())<=m){
			int temp = *s2.begin();
			s2.erase(s2.begin());
			s1.insert(temp);
			sum1 += temp;
			sum2 -= temp;
		}
		s1.insert(i);
		sum1 += i;
		cout<<s2.size()<<' ';
	}
	cout<<endl;
}


